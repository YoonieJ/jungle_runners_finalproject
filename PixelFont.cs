    // Draws text by stamping scaled pixels from the built-in 5x7 glyph table.
    public static void Draw(SpriteBatch spriteBatch, Texture2D pixel, string text, int x, int y, int scale, Color color)
    {
        int cursorX = x;
        foreach (char rawCharacter in text)
        {
            char character = char.ToUpperInvariant(rawCharacter);
            if (!Glyphs.TryGetValue(character, out string[]? glyph))
            {
                glyph = Glyphs[' '];
            }

            for (int row = 0; row < glyph.Length; row++)
            {
                for (int column = 0; column < glyph[row].Length; column++)
                {
                    if (glyph[row][column] == '1')
                    {
                        spriteBatch.Draw(pixel, new Rectangle(cursorX + column * scale, y + row * scale, scale, scale), color);
                    }
                }
            }

            cursorX += 6 * scale;
        }
    }
